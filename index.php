    var listing_id= $(this).attr("id");
    $.ajax({
      type:"POST",
      url:"ajax/delete_listing.php",
      data:{listing_id:listing_id},
      success:function (data) {
        if(data==1){
          $('#modal-delete').toggle();
          toastr.warning('Listing has been deleted!!!')
        }
      }
    });
})

